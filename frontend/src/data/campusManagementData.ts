export const getBuildingById = (buildingId: string) => buildings.find((b) => b.id === buildingId);

export const getFloorById = (floorId: string) => floors.find((floor) => floor.id === floorId);

export const getFloorsByBuildingId = (buildingId: string) =>
  floors.filter((floor) => floor.buildingId === buildingId).sort((a, b) => a.floorNumber - b.floorNumber);

export const getRoomsByBuildingId = (buildingId: string) =>
  rooms.filter((room) => room.buildingId === buildingId);

export const getRoomsByFloorId = (floorId: string) =>
  rooms.filter((room) => room.floorId === floorId);

export const getRoomById = (roomId: string) => rooms.find((room) => room.id === roomId);

export const calculateBuildingCapacity = (buildingId: string) =>
  getRoomsByBuildingId(buildingId).reduce((sum, room) => sum + room.maxOccupancy, 0);

export const getRoomTypeDistributionForBuilding = (buildingId: string) => {
  const buildingRooms = getRoomsByBuildingId(buildingId);
  return roomTypes
    .map((type) => ({ type, count: buildingRooms.filter((room) => room.type === type).length }))
    .filter((entry) => entry.count > 0)
    .sort((a, b) => b.count - a.count);
};
